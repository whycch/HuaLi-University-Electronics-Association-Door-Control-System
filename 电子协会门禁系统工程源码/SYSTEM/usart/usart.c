uint16_t cx=0,cy=0;
void data_deal(uint8_t *str,uint16_t LEN)
{
  uint16_t i=0,t=0;
  uint16_t temp_cx=0,temp_cy=0;
	  
  for(i=0;i<10;i++)
  {
    if(str[0]=='[')
    {
      if(str[i]==',')
        {
          t=1;              //判断是逗号标志
        }
        else
        {
          if(t)            //处理都好后面的数据，转字符串为整数
          {
            if(str[i]==']')
            {
              break ;
            }
            temp_cy=(str[i]-'0')+temp_cy*10;
          }
          else             //处理都好前面的数据，转字符串为整数
          {
            if(str[i]!='[')
            {
              temp_cx=(str[i]-'0')+temp_cx*10;
            }
            
          }
       }      
    }  
  }
  if((temp_cx!=0)&&(temp_cy!=0))
  {
    cx=temp_cx;
    cy=temp_cy;

  }
  
}


