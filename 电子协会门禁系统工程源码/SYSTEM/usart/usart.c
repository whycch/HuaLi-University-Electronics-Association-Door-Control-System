uint16_t uart_querychar(USART_TypeDef* USARTx,uint8_t *ch)
{
  /* Check the parameters */
  assert_param(IS_USART_ALL_PERIPH(USARTx));
  if(USART_GetFlagStatus(USARTx, USART_FLAG_RXNE) == SET)
	{
		 *ch=(uint16_t)(USARTx->DR & (uint16_t)0x01FF);
			return 1;
	}
  /* Receive Data */
	*ch=0;
  return 0;
}
/********************************

接收字符串

********************************/
uint16_t uart_querystr (USART_TypeDef* USARTx, uint8_t *str, uint8_t max_len)
{
    uint16_t i = 0, j;

    for(j = 0; j < 10000; j++)           // 10000 的作用是延时，可自行根据情况修改
    {
        while(uart_querychar(USARTx, str + i)  )
        {
            j = 0;
            if( *(str + i) == NULL )    //接收到字符串结束符
            {
                return i;
            }

            i++;
            if(i >= max_len)            //超过设定的最大值，退出
            {
                *(str + i) = 0;     //确保字符串结尾是0x00
                return i;
            }
        }
    }

    *(str + i) = 0;                     //确保字符串结尾是0x00
    return i;
}
/********************************

发送单个字符

********************************/
void uart_putchar(USART_TypeDef* USARTx, uint8_t Data)
{
  /* Check the parameters */
  assert_param(IS_USART_ALL_PERIPH(USARTx));
  assert_param(IS_USART_DATA(Data)); 
    
  /* Transmit Data */
  USARTx->DR = (Data & (uint16_t)0x01FF);
}
/********************************

发送字符串

********************************/
void uart_putbuff (USART_TypeDef* USARTx, uint8_t *buff, uint8_t len)
{
    while(len--)
    {
        uart_putchar(USARTx, *buff);
        buff++;
    }
}

